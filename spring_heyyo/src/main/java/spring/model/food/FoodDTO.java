package spring.model.food;

public class FoodDTO {

	private String food_code;
	private int f_price;
	private String f_name;
	private String f_image;
	private String f_menucode;
	private String f_content;
	private String r_code;
	
	public String getFood_code() {
		return food_code;
	}
	public void setFood_code(String food_code) {
		this.food_code = food_code;
	}
	public int getF_price() {
		return f_price;
	}
	public void setF_price(int f_price) {
		this.f_price = f_price;
	}
	public String getF_name() {
		return f_name;
	}
	public void setF_name(String f_name) {
		this.f_name = f_name;
	}
	public String getF_image() {
		return f_image;
	}
	public void setF_image(String f_image) {
		this.f_image = f_image;
	}
	public String getF_menucode() {
		return f_menucode;
	}
	public void setF_menucode(String f_menucode) {
		this.f_menucode = f_menucode;
	}
	public String getF_content() {
		return f_content;
	}
	public void setF_content(String f_content) {
		this.f_content = f_content;
	}
	public String getR_code() {
		return r_code;
	}
	public void setR_code(String r_code) {
		this.r_code = r_code;
	}
	
}
